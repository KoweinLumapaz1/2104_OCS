    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            int C;
            jRadioButton1.setActionCommand("Round");
            jRadioButton2.setActionCommand("Rectangle");
            jRadioButton3.setActionCommand("Square");
            jRadioButton4.setActionCommand("Aviator");
            jRadioButton5.setActionCommand("Oval");
            jRadioButton6.setActionCommand("Geometric");
            jRadioButton7.setActionCommand("Browline");
            
            
            String FrameShape = buttonGroup1.getSelection().getActionCommand();
            System.out.print(FrameShape);
            String GradeLvl = jTextField2.getText();
            String Quantity = jTextField3.getText();
            String Inserter = ("INSERT INTO `classes` (`frameShape`, `GradeLevel`, `qty`) VALUES (?,?,? )");
            
            pst = connect.prepareStatement(Inserter);

            pst.setString(1, FrameShape);
            pst.setString(2, GradeLvl);
            pst.setString(3, Quantity);
            


            int d = JOptionPane.showConfirmDialog(null, "Confirm Addition?");
            if(d == JOptionPane.YES_OPTION){
                C = pst.executeUpdate();
                if (C==1){
                    JOptionPane.showMessageDialog(null, "Recorded Successfully");
                    
                    this.dispose();
                }

            }      
        } catch (SQLException ex) {
            Logger.getLogger(AddStock.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

